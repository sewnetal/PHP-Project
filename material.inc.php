            <p><h1>Abstract</h1>
      <p></p>
<p>This study material will be thoroughly depth into explanation of the Usability, focuses on the Website analysis.It will cover definitions of</p>
<p>Usability as defined by different authors, the ISO standard and comparison will be made based on the key notes from each definition.</p>
<p>This tutorial will teach how to conduct Website Usability, and gives justifications on the importance of conductingUsability and also when is</p>
<p>needed to conduct Usability. Also, it will give insight into Website Usability testing which will be the area of focus for the remaining</p>
<p>part of this tutorial and brief introduction into the Usability evaluationas this part will be fully treated in other material. </p>
<p><h2>1 INTRODUCTION</h2>
      <p></p>
<p>It has become imperative to website designers/developers to embrace Website Usability as part of development cycle.The result of this idea has been proven</p>
<p>productive over the year, as this facilitates the efficiency, effectiveness, learnability and satisfaction of users of such sites. Though this material will base</p>
<p>on Website Usability yet there are other areas such as software development Usability, mobile device Usability, none of this will be discussed in this material.</p>
<p>At this junction, it will be necessary to define Usability in the area of how, when, where can you improve it? Why should you care? This overview answers these </p>
<p>basic questions which will be the points that will be emphasized throughout this material.</p>
<h2>2 DEFINITION OF USABILITY</h2>
      <p></p>
<p>In order to gain full understanding of Usability, we are going to look at series of definitions from different authors’ point of views.</p>
<ul>
	<li><p>Usability is an approach to product development that incorporates direct user feedback throughout the development cycle in order to reduce costs and</p>
	<p>create products and tools that meet user needs. (Usability Professionals´ Association, 2011).</p> 
	<li><p>As defined by international standards: Usability refers to the extent to which a product can be used by specified users to achieve specified goals with </p>
	<p>effectiveness, efficiency and satisfaction in a specified context of use. (ISO 92411-11).</p>
	<li><p>According to Jakob Nielsen: Usability is a quality attribute that assesses how easy user interfaces are to use. The word “usability” also refers to method </p>
	<p>for improving ease-of-use during the design process. (Jakob N.,2003) </p></li>
</ul>
      <p></p>
<p>Looking through the above definitions, it can be deduced that Usability is defined around five quality components:</p>
<ul>
	<li><p><span ?style1?><b>Learnability:	</b></span> How easy is it for users to achieve basic tasks the first time of using the design?</p>
	<li><p><span ?style1?><b>Efficiency:		</b></span> Once users have learned the design, how fast can they implement the tasks?</p>
	<li><p><span ?style1?><b>Memorability:	</b></span> How easily can users reestablish proficiency after a long period of using the design?</p>
	<li><p><span ?style1?><b>Error:			</b></span> How often do users make errors and how easily can they recover from the errors?</p>
	<li><p><span ?style1?><b>Satisfaction:	</b></span> How much users enjoy using the design?</p></li>
</ul>
<h1><p>Why – Importance of Usability</p></h1>
<p>No user will settle for a Web site that demands reading a manual before using it. If a website is difficult to use, people will definitely leave. If the homepage fails to </p>
<p>clearly state what a company offers and what users can do on the site, people leave. With reference to the first law of e-commerce, it has been established that</p>
<p>if users cannot easily locate the product, they cannot buy it either. With above scenarios, it shows that usability is a necessary condition for better performance of </p>
<p>a website. However, current best practices demand spending about 10% of a design project´s budget on usability. This practice doubles the desired quality metrics of </p>
<p>such design.</p>
